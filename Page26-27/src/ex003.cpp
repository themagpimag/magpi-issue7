#include <iostream>
using namespace std;

int main()
{
  // Create 2 variables
  int a, b;

  // Ask for and store user input
  cout << "Input the first number: ";
  cin >> a;
  
  cout << "Input the second number: ";
  cin >> b;
  
  // Output the sum of these variables
  cout << a << " + " << b << " = " << a + b << endl;
  return 0;
}
